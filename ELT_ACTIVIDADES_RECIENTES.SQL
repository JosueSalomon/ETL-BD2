CREATE OR REPLACE PROCEDURE ELT_ACTIVIDADES_RECIENTES AS
    V_FECHA_INICIO DATE;
    V_FECHA_FIN DATE := TRUNC(SYSDATE); -- Fecha actual
    V_HORA_INICIO_PROCESO DATE := SYSDATE;
BEGIN
    EXECUTE IMMEDIATE 'TRUNCATE TABLE C##DWH.TBL_ACTIVIDADES_RECIENTES';
    
    SELECT MIN(FECHA_ACTIVIDAD) INTO V_FECHA_INICIO  FROM TBL_ACTIVIDADES_RECIENTES;
    
    IF V_FECHA_INICIO IS NULL THEN
        DBMS_OUTPUT.PUT_LINE('No hay datos para procesar.');
        RETURN;
    END IF;
    
    WHILE V_FECHA_INICIO <= V_FECHA_FIN LOOP
    
    DELETE FROM C##DWH.TBL_ACTIVIDADES_RECIENTES
    WHERE TRUNC(FECHA_ACTIVIDAD) =TRUNC(V_FECHA_INICIO);
    
    INSERT INTO C##DWH.TBL_ACTIVIDADES_RECIENTES(
        ID_ACTIVIDAD,
        ID_USAURIO,
        ACTIVIDAD,
        FECHA_ACTIVIDAD
    )
    SELECT ID_ACTIVIDAD,
        ID_USUARIO,
        ACTIVIDAD,
        FECHA_ACTIVIDAD
    FROM TBL_ACTIVIDADES_RECIENTES
    WHERE(FECHA_ACTIVIDAD) = TRUNC(V_FECHA_INICIO);
    
    COMMIT;
         V_FECHA_INICIO := V_FECHA_INICIO + 1;
    END LOOP;
      P_ETL_LOG(
        P_NOMBRE_ETL => $$PLSQL_UNIT,
        P_FECHA_HORA_INICIO => sysdate,
        P_ESTATUS => 'S',
        P_ERROR => ''
    );
    -- Hacer commit para confirmar los cambios en la base de datos
    COMMIT;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('No se encontraron datos.');
        -- Agregar al log de errores
        P_ETL_LOG(
            P_NOMBRE_ETL => $$PLSQL_UNIT,
            P_FECHA_HORA_INICIO => sysdate,
            P_ESTATUS => 'F',
            P_ERROR => SQLCODE || ' - ' || SQLERRM
        );


END ELT_ACTIVIDADES_RECIENTES;

BEGIN
    ELT_ACTIVIDADES_RECIENTES;
END;

